const loginUser = async (req, res) => {
  try {
    const { email, password } = req.body;

    if (!email || !password) {
      return res.status(400).json({
        success: false,
        message: "Email and password are required",
      });
    }

    const normalizedEmail = email.toLowerCase().trim();

    const user = await UserModel.findOne({ email: normalizedEmail });

    if (!user) {
      return res.status(401).json({
        success: false,
        message: "Invalid email or password",
      });
    }

    // üîí Check if account is active
    if (!user.isActive) {
      return res.status(403).json({
        success: false,
        message: "Account is deactivated",
      });
    }

    // üîí Check if account is locked
    if (user.lockUntil && user.lockUntil > Date.now()) {
      const remainingMs = user.lockUntil - Date.now();
      const remainingSeconds = Math.ceil(remainingMs / 1000);

      return res.status(423).json({
        success: false,
        message: "Account temporarily locked",
        remainingSeconds,
      });
    }

    // üîë Compare password
    const isMatch = await comparePassword(password, user.password);

    if (!isMatch) {
      user.loginAttempts += 1;

      if (user.loginAttempts >= 3) {
        user.lockUntil = Date.now() + 5 * 60 * 1000;
        user.loginAttempts = 0;
        await user.save();

        return res.status(423).json({
          success: false,
          message: "Account temporarily locked",
          remainingSeconds: 5 * 60,
        });
      }

      await user.save();

      return res.status(401).json({
        success: false,
        message: "Invalid email or password",
      });
    }

    // Reset login attempts on success
    user.loginAttempts = 0;
    user.lockUntil = null;

    const token = jwt.sign(
      {
        _id: user._id,
        role: user.role,
      },
      process.env.JWT_SECURE,
      {
        expiresIn: "12h",
      },
    );

    // üîÅ Force password change check
    if (user.mustChangePassword) {
      await user.save();
      console.log("this block comes");

      return res.status(403).json({
        success: false,
        message: "Password change required",
        forcePasswordChange: true,
        token,
      });
    }

    // üïí Check password expiry (7 days rule)
    // const sevenDays = 2 * 60 * 1000;
    const sevenDays = 7 * 24 * 60 * 60 * 1000;
    if (Date.now() - user.passwordChangedAt.getTime() > sevenDays) {
      const token = jwt.sign(
        {
          _id: user._id,
          role: user.role,
          passwordExpired: true, // üî• important flag
        },
        process.env.JWT_SECURE,
        { expiresIn: "2m" }, // short expiry
      );
      return res.status(403).json({
        success: false,
        message: "Password expired. Please change it.",
        forcePasswordChange: true,
        token,
      });
    }

    await user.save();

    return res.status(200).json({
      success: true,
      message: "Login successful",
      user: {
        id: user._id,
        name: user.name,
        email: user.email,
        role: user.role,
      },
      token,
    });
  } catch (error) {
    console.error("Login error:", error);
    return res.status(500).json({
      success: false,
      message: "Internal server error",
    });
  }
};

